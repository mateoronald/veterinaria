package com.google.buscador.venta.bean;

public class DistritoBean {

	private String iddistrito,des_distrito;

	public DistritoBean() {
		super();
	}

	public DistritoBean(String iddistrito, String des_distrito) {
		super();
		this.iddistrito = iddistrito;
		this.des_distrito = des_distrito;
	}

	

	public String getIddistrito() {
		return iddistrito;
	}

	public void setIddistrito(String iddistrito) {
		this.iddistrito = iddistrito;
	}

	public String getDes_distrito() {
		return des_distrito;
	}

	public void setDes_distrito(String des_distrito) {
		this.des_distrito = des_distrito;
	}
	

	
	
}
